void WorkWithIntegers()
{
    int a = 18;
    int b = 6;
    int c = a + b;
    Console.WriteLine(c);


    // subtraction
    c = a - b;
    Console.WriteLine(c);

    // multiplication
    c = a * b;
    Console.WriteLine(c);

    // division
    c = a / b;
    Console.WriteLine(c);
}

void OrderPrecedence()
{
double a = 14;
double b = 21;
double c = 10;
double d = (a + b) / c;
Console.WriteLine(d);

double third = 1.0 / 3.0;
Console.WriteLine(third);
}